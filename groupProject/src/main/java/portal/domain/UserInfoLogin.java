package portal.domain;

public class UserInfoLogin {
	String username;
	String password;
	
	public String getUsername() {
		return username;
	}
	public void setLogin(String login) {
		this.username = login;
	}
	public String getPassword() {
		return password;
	}
	public void setPassword(String password) {
		this.password = password;
	}
}
